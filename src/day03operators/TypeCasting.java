package day03operators;

public class TypeCasting {

	public static void main(String[] args) {
		
		
		    byte sayi1 = 50;
		    int in1 = sayi1;
		    System.out.println(in1);
		    
		    double sayi2 = 7.6;
		    int in2 = (int) sayi2;
		    System.out.println(in2);
		    
		    float sayi3 = 7.5f;
		    short sh1 = (short) sayi3;
		    System.out.println(sh1);

	}

}
