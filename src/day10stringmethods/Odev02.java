package day10stringmethods;

public class Odev02 {

	public static void main(String[] args) {
		
		String str1 = "";
		
		System.out.println(str1.isEmpty()); // true
		
		String str2 = " ";
		
		System.out.println(str2.isEmpty()); // false
		
		String str3 = "Almanya";
		
		System.out.println(str3.isEmpty()); // false
		
		

	}

}
